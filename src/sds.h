/**
 * char*类型的别名sds
 */
typedef char *sds;

/**
 * 3.2分支之前的sdshdr结构
 * struct sdshdr {
 *    unsigned int len; // buf数组中已使用字节的数量
 *    unsigned int free; // buf数组中未使用字节的数量
 *    char buf[]; // 保存字符串的字节数组
 * }
 *
 * 3.2分支引入了五种sdshdr类型，每次创建时根据sds的实际长度判断选择什么类型的sdshdr，不同类型的sdshdr占用的空间不同。
 */

/* Note: sdshdr5 is never used, we just access the flags byte directly.
 * However is here to document the layout of type 5 SDS strings. */
/**
 * sdshdr5永远不会被使用（直接访问标志字节？）
 */
struct __attribute__ ((__packed__)) sdshdr5 {
    unsigned char flags; /* 3 lsb of type, and 5 msb of string length */ /* 低3位存真实的flags（类型），高5位存len（长度） */
    char buf[]; /* 字符串字节数组 */
};

/**
 * __attribute__ ((__packed__))语法是GCC的一个扩展，不存在于任何C语言标准，用来告诉编译器使用最小的内存来存储sdshdr.
 */
struct __attribute__ ((__packed__)) sdshdr8 {
    uint8_t len; /* used */ /* 字符串长度 */
    uint8_t alloc; /* excluding the header and null terminator */ /* 已分配的内存大小（单位是字节） */
    unsigned char flags; /* 3 lsb of type, 5 unused bits */ /* 用一个字节表示当前sdshdr的类型，因为sdshdr有5种类型，所以至少需要3位来表示。000:sdshdr5, 001:sdshdr8, 010:sdshdr16, 011:sdshdr32, 100:sdshdr64。高5位用不到都为0 */
    char buf[];
};
struct __attribute__ ((__packed__)) sdshdr16 {
    uint16_t len; /* used */
    uint16_t alloc; /* excluding the header and null terminator */
    unsigned char flags; /* 3 lsb of type, 5 unused bits */
    char buf[];
};
struct __attribute__ ((__packed__)) sdshdr32 {
    uint32_t len; /* used */
    uint32_t alloc; /* excluding the header and null terminator */
    unsigned char flags; /* 3 lsb of type, 5 unused bits */
    char buf[];
};
struct __attribute__ ((__packed__)) sdshdr64 {
    uint64_t len; /* used */
    uint64_t alloc; /* excluding the header and null terminator */
    unsigned char flags; /* 3 lsb of type, 5 unused bits */
    char buf[];
};

#define SDS_TYPE_5  0 // 00000000
#define SDS_TYPE_8  1 // 00000001
#define SDS_TYPE_16 2 // 00000010
#define SDS_TYPE_32 3 // 00000011
#define SDS_TYPE_64 4 // 00000100
#define SDS_TYPE_MASK 7 // 00000111 作为取flag低三位的掩码,主要由于sdshdr5的高5位不一定为0，所以需要有SDS_TYPE_MASK
#define SDS_TYPE_BITS 3
#define SDS_HDR_VAR(T,s) struct sdshdr##T *sh = (void*)((s)-(sizeof(struct sdshdr##T)));
#define SDS_HDR(T,s) ((struct sdshdr##T *)((s)-(sizeof(struct sdshdr##T))))  // 返回一个类型为T包含s字符串的sdshdr的指针。##的含义是在一个宏定义里连接两个子串，##两边的子串会被编译器识别为一个。
#define SDS_TYPE_5_LEN(f) ((f)>>SDS_TYPE_BITS) // 用sdshdr5的flag变量做参数返回sds的长度,右移3位，剩下的高5位就是sds长度

static inline size_t sdslen(const sds s) {
    // s[-1]等同于*(s-1)，sdshdr内部禁用了内存对齐，所以此处取出的是flag？
    unsigned char flags = s[-1];
    // 根据flags和SDS_TYPE_MASK来判断sdshdr的类型，再按照指定类型获取长度
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5:
            return SDS_TYPE_5_LEN(flags);
        case SDS_TYPE_8:
            return SDS_HDR(8,s)->len;
        case SDS_TYPE_16:
            return SDS_HDR(16,s)->len;
        case SDS_TYPE_32:
            return SDS_HDR(32,s)->len;
        case SDS_TYPE_64:
            return SDS_HDR(64,s)->len;
    }
    return 0;
}

static inline size_t sdsavail(const sds s) {
    unsigned char flags = s[-1];
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5: {
            return 0;
        }
        case SDS_TYPE_8: {
            SDS_HDR_VAR(8,s);
            return sh->alloc - sh->len;
        }
        case SDS_TYPE_16: {
            SDS_HDR_VAR(16,s);
            return sh->alloc - sh->len;
        }
        case SDS_TYPE_32: {
            SDS_HDR_VAR(32,s);
            return sh->alloc - sh->len;
        }
        case SDS_TYPE_64: {
            SDS_HDR_VAR(64,s);
            return sh->alloc - sh->len;
        }
    }
    return 0;
}

static inline void sdssetlen(sds s, size_t newlen) {
    unsigned char flags = s[-1];
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5:
            {
                unsigned char *fp = ((unsigned char*)s)-1;
                *fp = SDS_TYPE_5 | (newlen << SDS_TYPE_BITS);
            }
            break;
        case SDS_TYPE_8:
            SDS_HDR(8,s)->len = newlen;
            break;
        case SDS_TYPE_16:
            SDS_HDR(16,s)->len = newlen;
            break;
        case SDS_TYPE_32:
            SDS_HDR(32,s)->len = newlen;
            break;
        case SDS_TYPE_64:
            SDS_HDR(64,s)->len = newlen;
            break;
    }
}

static inline void sdsinclen(sds s, size_t inc) {
    unsigned char flags = s[-1];
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5:
            {
                unsigned char *fp = ((unsigned char*)s)-1;
                unsigned char newlen = SDS_TYPE_5_LEN(flags)+inc;
                *fp = SDS_TYPE_5 | (newlen << SDS_TYPE_BITS);
            }
            break;
        case SDS_TYPE_8:
            SDS_HDR(8,s)->len += inc;
            break;
        case SDS_TYPE_16:
            SDS_HDR(16,s)->len += inc;
            break;
        case SDS_TYPE_32:
            SDS_HDR(32,s)->len += inc;
            break;
        case SDS_TYPE_64:
            SDS_HDR(64,s)->len += inc;
            break;
    }
}

/* sdsalloc() = sdsavail() + sdslen() */
static inline size_t sdsalloc(const sds s) {
    unsigned char flags = s[-1];
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5:
            return SDS_TYPE_5_LEN(flags);
        case SDS_TYPE_8:
            return SDS_HDR(8,s)->alloc;
        case SDS_TYPE_16:
            return SDS_HDR(16,s)->alloc;
        case SDS_TYPE_32:
            return SDS_HDR(32,s)->alloc;
        case SDS_TYPE_64:
            return SDS_HDR(64,s)->alloc;
    }
    return 0;
}

static inline void sdssetalloc(sds s, size_t newlen) {
    unsigned char flags = s[-1];
    switch(flags&SDS_TYPE_MASK) {
        case SDS_TYPE_5:
            /* Nothing to do, this type has no total allocation info. */
            break;
        case SDS_TYPE_8:
            SDS_HDR(8,s)->alloc = newlen;
            break;
        case SDS_TYPE_16:
            SDS_HDR(16,s)->alloc = newlen;
            break;
        case SDS_TYPE_32:
            SDS_HDR(32,s)->alloc = newlen;
            break;
        case SDS_TYPE_64:
            SDS_HDR(64,s)->alloc = newlen;
            break;
    }
}

